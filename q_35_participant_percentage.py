print("Last year, about 2,400 people participated in a local event. This year, about 3,200 people participated. What was the approximate percent increase in participation?")

initial_participants = 2400
subsequent_participants = 3200

difference_between_the_participants = subsequent_participants-initial_participants

print(f"The difference between the participants {difference_between_the_participants}")

percentage = (difference_between_the_participants/initial_participants)*100

print(f"There has been an approximate {percentage} percent increase.")