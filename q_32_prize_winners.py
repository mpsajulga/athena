print("In a writing competition, the first-place winner receives ½ of the prize money. The second runner up receives ¼ of what the winner won. What was the total amount of prize money distributed if the winner receives $8,000?")

first_place_prize = 8000
second_place = 1/4

second_place_prize = first_place_prize*second_place
total_prize = first_place_prize*2 #because one half
print(f"The first place winner gets {first_place_prize} as the first place prize.")
print(f"The second place runner up gets {second_place_prize}.")
print(f"The total prize amounts to {total_prize}.")